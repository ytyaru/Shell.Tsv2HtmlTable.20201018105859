#---------------------------------------------------------------------------
# HTMLタグで囲む。
# CreatedAt: 2020-10-18
#---------------------------------------------------------------------------
Enclose() { echo -n '<'"$1"'>'"$2"'</'"$1"'>'; }
Encloses() {
	cat - | while read line; do {
		Enclose "$1" "$line"
	} done;
}
